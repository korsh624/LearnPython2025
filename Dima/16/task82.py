'''
83)	Алгоритм вычисления функции F(n), где n – целое число, задан следующими соотношениями:
F(n) = n, при n <= 5,
F(n) = n + F(n / 2 – 3), когда n > 5 и делится на 8,
F(n) = n + F(n + 4) , когда n > 5 и не делится на 8.
Назовите максимальное значение n, для которого возможно вычислить F(n).

'''
import sys
# Устанавливаем строгий лимит рекурсии (чтобы не висла программа)
sys.setrecursionlimit(1000)

def F(n):
    """Вычисляет F(n) по заданным правилам."""
    if n <= 5:
        return n
    elif n % 8 == 0:
        return n + F(n // 2 - 3)
    else:  # n > 5 и не делится на 8
        return n + F(n + 4)


def find_max_n(start=1000, end=1):
    """Находит максимальное n, для которого F(n) вычисляется."""
    for n in range(start, end - 1, -1):  # от большого к маленькому
        try:
            F(n)
            return True
        except RecursionError:
            return False

# Основной код
max_n = find_max_n()
print(max_n)
