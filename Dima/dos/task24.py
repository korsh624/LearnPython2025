'''Определите максимальное количество идущих подряд символов в прилагаемом файле, 
среди которых пара символов BС (в указанном порядке) встречается не более 180 раз.
Для выполнения этого задания следует написать программу.'''
f = open('24.txt').read().strip()  # Читаем файл и убираем лишние пробелы/переводы строк

left = 0  # Левый указатель окна
count_bc = 0  # Счетчик вхождений "BC" в текущем окне
max_len = 0  # Ответ: максимальная длина подходящей подстроки

for right in range(len(f)):
    # Если нашли пару BC, увеличиваем счетчик
    if right > 0 and f[right - 1] == 'B' and f[right] == 'C':
        count_bc += 1

    # Если превысили лимит (180), сдвигаем левый указатель
    while count_bc > 180:
        if f[left] == 'B' and (left + 1 < len(f) and f[left + 1] == 'C'):
            count_bc -= 1  # Убираем пару BC, которая уходит из окна
        left += 1

    # Обновляем максимум длины текущего допустимого окна
    max_len = max(max_len, right - left + 1)

print(max_len)
