'''
В файле содержится последовательность целых чисел.
Элементы последовательности могут принимать значения от 1 до 10000.
Определите количество пар элементов последовательности,
в которых оба числа нечетны и их среднее арифметическое присутствует
в файле, а также определите максимальное среднее арифметическое
элементов одной из таких пар.
Парой подразумевается два идущих подряд элемента последовательности.
В ответе запишите через пробел сначала количество, затем максимальное
среднее арифметическое.
'''
# Открываем файл и читаем данные
with open('17_1.txt', 'r') as file:
    lines = file.readlines()
    numbers = []
    for line in lines:
        numbers.append(int(line))
'''
# Создаём множество для быстрой проверки наличия чисел
num_set = set(numbers)
'''

# Инициализируем переменные для подсчёта
count = 0
max_average = 0

# Проходим по всем парам подряд идущих элементов
for i in range(len(numbers) - 1):
    a, b = numbers[i], numbers[i + 1]

    # Проверяем, что оба числа нечётные
    if a % 2 != 0 and b % 2 != 0:
        # Вычисляем среднее арифметическое
        average = (a + b) // 2

        # Проверяем, есть ли среднее в множестве чисел
        if average in numbers:
            count += 1
            # Обновляем максимальное среднее, если текущее больше
            if average > max_average:
                max_average = average

# Выводим результат: количество пар и максимальное среднее
print(count, max_average)
