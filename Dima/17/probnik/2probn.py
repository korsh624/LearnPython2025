# Считываем числа из файла в список
s = [int(i) for i in open('17.txt')]

# Находим максимальный отрицательный трёхзначный элемент, кратный 6
maxi_3 = max([i for i in s if
              len(str(abs(i))) == 3 and  # трёхзначный по модулю
              i < 0 and                  # отрицательный
              abs(i) % 6 == 0])         # кратен 6

# Список для хранения сумм квадратов подходящих пар
otv = []

# Перебираем все пары соседних элементов
for i in range(len(s) - 1):
    # Проверяем условие: только одно число в паре отрицательное
    if (s[i] < 0 and s[i + 1] >= 0) or (s[i] >= 0 and s[i + 1] < 0):
        # Проверяем, что сумма пары больше найденного максимального элемента
        if (s[i] + s[i + 1]) > maxi_3:
            # Добавляем сумму квадратов элементов пары в список
            otv.append(s[i]**2 + s[i + 1]**2)

# Выводим результат: количество пар и максимальную сумму квадратов
print(len(otv), max(otv))
