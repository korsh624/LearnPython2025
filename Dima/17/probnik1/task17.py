'''
Элементы последовательности могут принимать целые значения от –10 000 до 10 000 включительно.
Определите количество пар последовательности, в которых только одно число оканчивается на 3,
а квадрат суммы элементов пары не меньше модуля минимального элемента последовательности,
оканчивающегося на 3. В ответе запишите два числа: сначала количество найденных пар, затем, через пробел,
максимальный из квадратов сумм элементов таких пар. В данной задаче под парой подразумевается два идущих подряд
элемента последовательности.
'''
# Чтение данных (пример — из файла 17.txt)
with open('17.txt', 'r') as file:
    lines = file.readlines()
    numbers = []
    for line in lines:
        numbers.append(int(line))

# Шаг 1: найти минимальный элемент, оканчивающийся на 3
min_elem_with_3 = None
for num in numbers:
    if abs(num) % 10 == 3:
        if min_elem_with_3 is None or num < min_elem_with_3:
            min_elem_with_3 = num

# Если нет элементов, оканчивающихся на 3, ответ 0 0
if min_elem_with_3 is None:
    print("0 0")
else:
    M = abs(min_elem_with_3)  # модуль минимального элемента
    count = 0  # счётчик подходящих пар
    max_square_sum = 0  # максимальный квадрат суммы

    # Шаг 2: перебор пар соседних элементов
    for i in range(len(numbers) - 1):
        a, b = numbers[i], numbers[i + 1]

        # Условие 1: только одно число оканчивается на 3
        cond1 = (abs(a) % 10 == 3) != (abs(b) % 10 == 3)

        # Условие 2: квадрат суммы >= M
        square_sum = (a + b) ** 2
        cond2 = square_sum >= M

        if cond1 and cond2:
            count += 1
            if square_sum > max_square_sum:
                max_square_sum = square_sum

    print(f"{count} {max_square_sum}")
