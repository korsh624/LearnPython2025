'''
30)	(А. Куканова) Рассматривается множество целых чисел, принадлежащих числовому отрезку [1529; 9482], которые удовлетворяют следующим условиям:
− запись в двоичной системе закачивается на 01;
− запись в пятеричной системе заканчивается на 3.
Найдите минимальное из таких чисел и их сумму. Гарантируется, что искомая сумма не превосходит 107.

'''
min_num = None  # Минимальное подходящее число
total_sum = 0   # Сумма всех подходящих чисел

# Перебираем все числа в заданном диапазоне
for x in range(1529, 9483):  # 9483, потому что range не включает верхнюю границу
    # Проверяем условия
    if x % 4 == 1 and x % 5 == 3:
        if min_num is None:
            min_num = x  # Первое найденное число — минимальное
        total_sum += x

# Выводим результаты
print("Минимальное число:", min_num)
print("Сумма всех таких чисел:", total_sum)
